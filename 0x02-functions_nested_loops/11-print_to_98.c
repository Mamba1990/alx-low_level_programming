#include "main.h"
#include <stdio.h>
/**
* print_to_98 - prints all natural numbers from n to 98
*@n: Input  number of an integer
*
*Description: 'the program's description'
*Return: natural numbers
*/
void print_to_98(int n)
{
	if (n <= 98)
	{
	for (; n <= 98; n++)
	{
	if (n == 98)
	{
	printf("%d", n);
	printf("\n");
	break;
	}
	else
	{
	printf("%d, ", n);
	}
	}
	}
	else
	{
	for (; n <= 98; n--)
	{
	if (n == 98)
	{
	printf("%d", n);
	printf("\n");
	break;
	}
	else
	{
	printf("%d, ", n);
	}
	}
	}
}

